% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
global a;global skw;global en;global k; 
[fname,path]=uigetfile('*.*','Browse Image');
if fname~=0
    a=imread([path,fname]);
    axes(handles.axes1); imshow(a); title('Original Image');
else
    warndlg('Please Select the necessary Image File');
end
 
imwrite(a,'1.png');
run('Analysis.m');
m=mean2(a);%mean
sd=std2(a);%std dev
en=entropy(a);%entropy
skw=skewness(a(:));%skewness
k=kurtosis(a(:));
set(handles.edit2,'String',m);
set(handles.edit3,'String',sd);
set(handles.edit4,'String',en);
set(handles.edit5,'String',k);
set(handles.edit6,'String',skw);
acc=accuracy_image(a);
sen=Sensitivity_image(a);
spe=Specificity_image(a); 
set(handles.edit9,'String',acc);
set(handles.edit10,'String',sen);
set(handles.edit11,'String',spe);
 






